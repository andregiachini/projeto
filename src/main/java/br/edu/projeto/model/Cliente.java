package br.edu.projeto.model;

public class Cliente {
	
	//Atributos
	private String cpf_cliente;
	private String nome_cliente;
	private String nome_social_cliente;
	private double altura_cliente;
	private double massa_cliente;
	private String genero_cliente;
	private int idade_cliente;
	private String email_cliente;
	private String celular_cliente;
	private String endereco;
	
	
	public String getCpf_cliente() {
		return cpf_cliente;
	}
	public void setCpf_cliente(String cpf_cliente) {
		this.cpf_cliente = cpf_cliente;
	}
	public String getNome_cliente() {
		return nome_cliente;
	}
	public void setNome_cliente(String nome_cliente) {
		this.nome_cliente = nome_cliente;
	}
	public String getNome_social_cliente() {
		return nome_social_cliente;
	}
	public void setNome_social_cliente(String nome_social_cliente) {
		this.nome_social_cliente = nome_social_cliente;
	}
	public double getAltura_cliente() {
		return altura_cliente;
	}
	public void setAltura_cliente(double altura_cliente) {
		this.altura_cliente = altura_cliente;
	}
	public double getMassa_cliente() {
		return massa_cliente;
	}
	public void setMassa_cliente(double massa_cliente) {
		this.massa_cliente = massa_cliente;
	}
	public String getGenero_cliente() {
		return genero_cliente;
	}
	public void setGenero_cliente(String genero_cliente) {
		this.genero_cliente = genero_cliente;
	}
	public int getIdade_cliente() {
		return idade_cliente;
	}
	public void setIdade_cliente(int idade_cliente) {
		this.idade_cliente = idade_cliente;
	}
	public String getEmail_cliente() {
		return email_cliente;
	}
	public void setEmail_cliente(String email_cliente) {
		this.email_cliente = email_cliente;
	}
	public String getCelular_cliente() {
		return celular_cliente;
	}
	public void setCelular_cliente(String celular_cliente) {
		this.celular_cliente = celular_cliente;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	
	
	
	

}
